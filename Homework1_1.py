mood = input("how are you? ")

if mood == "happy":
    print("i am glad that you are happy!")
elif mood == "nervous":
    print("calm down, please!")
elif mood == "relaxed":
    print("that's how it should be!")
elif mood == "sad":
    print("it's gonna get better soon!")
elif mood == "excited":
    print("yeah, i am too!")

else:
    print("I don't recognize this mood")
